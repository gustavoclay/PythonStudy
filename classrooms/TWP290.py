#for each
print ("FOR:")
for letra in 'aeiou':
    print (letra)

#while
print ("WHILE:")
texto = 'aeiou'
k = 0
while k < len(texto):
    letra = texto[k]
    print (letra)
    k += 1

#for each
for i in range(5):
    print(i)

#while
lista = list(range(5))
k = 0
while k < len(lista):
    i = lista[k]
    print (i)
    k += 1